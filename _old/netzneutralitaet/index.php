<h2 class="cat">Netzneutralität bedeutet ...</h2>

<div class="definition">
	<div style="width:48%; margin-right:4%; float:left;">
		<h3>Gleichbehandlung</h3>
		<p>Alle Datenpakete müssen während der Übertragung unabhängig von Herkunft, Inhalt, Ziel, Klasse oder Tarif gleich behandelt werden. Netzwerkmanagement ist nur aus technischen Gründen zur Erhaltung der Stablität des Netzwerkes zulässig. Netzwerkeingriffe dürfen nicht geschäftsmodellabhängig oder politisch motiviert sein.</p>

		<h3>Teilhabe</h3>
		<p> Ein Internetanschluss muss die volle Teilhabe am Netz ermöglichen. Diese Teilhabe inkludiert das Nutzen und Bereitstellen von beliebigen Diensten unabhängig von verwendeten Technologien.</p>

		<h3>Zugang</h3>
		<p>Der Zugangsanbieter darf keine technischen oder rechtlichen Einschränkungen bezüglich der vom Endkunden angeschlossenen Peripherie (Hardware und Software) zu seinem Netz vorgeben. </p>

		<h3>Unversehrtheit der Daten</h3>
		<p>Eingriffe in die Arbeitsweise von Protokollen oder die Manipulation des Inhalts einer Datenübertragung sind nicht zulässig.</p>

	</div>

	<div style="width:48%; float:left;">
		

		<h3>Transparenz</h3>
		<p>Zugangsanbieter müssen in ihren Verträgen und Angeboten eine zugesicherte Mindest-Bandbreite und zugesicherte Maximal-Latenzen, sowie die darauf anzuwendenden Qualitätsmaßgaben offen legen und genormte Werkzeuge für deren Überprüfung durch Dritte zur Verfügung stellen. Diese Qualitätsmaßgaben müssen aufgrund einer klar definierten statistischen Analyse erhoben und veröffentlicht werden und nachprüfbar sein. Eingriffe in das Netzwerkmanagement (Verstöße gegen Punkt 1 Gleichbehandlung) müssen mit einer Begründung für die Stablität des Netzes dem Kunden und der Regulierungsbehörde offen gelegt werden.</p>

		<h3>Bandbreiten-Garantie</h3>
		<p>Die vertraglich festgelegten und in den Angeboten beworbenen Bandbreiten, Latenzen und Qualitätsmaßgaben müssen eingehalten werden. </p>

		<h3>Durchsetzung</h3>
		<p>Verstöße gegen die oben beschriebenen Bedingungen können vom Endkunden an eine vom Gesetzgeber zu definierende Stelle gemeldet werden, welche mit den notwendigen Durchgriffsrechten zur Durchsetzung der Bestimmungen ausgestattet ist. Der Gesetzgeber garantiert, dass die verstoßende Partei auch zur Verantwortung gezogen werden kann.</p>
		</div>
		
